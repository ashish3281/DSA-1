
public class TypeCasting {
    public static void main(String args[]){
        //wideing type casting
        int myInt=9;
        double myDouble=myInt;
        System.out.println(myInt);
        System.out.println(myDouble);
        //narrow type casting
        double x=9.78d;
        int y=(int)x;
        System.out.println(x);
        System.out.println(y);
    }
}
